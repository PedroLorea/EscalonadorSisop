public class ComandosAritmeticos {

    public int add (int acc, int op1){
        return acc + op1;
    }

    public int sub (int acc, int op1){
        return acc - op1;
    }

    public int mult (int acc, int op1){
        return acc * op1;
    }

    public int div (int acc, int op1){
        return acc / op1;
    }
}
